<h1 class="text-center">Nosotros</h1>
<section class="container bg-light container-md my-4">
    <div class="py-5 fw-bold h1 text-center"
    style="color:var(--bs-info-text-emphasis); text-decoration: underline">NUESTROS INICIOS</div>
    <div class="py-3 h5" style="color: black">
    <p class="text-justify">
        Instrumentos musicales, inicia como una empresa familiar a mediados de los años 80 cuando el abuelo de nuestro CEO, Edmundo Navarro Alperio, decidió fabricar instrumentos en el taller de su casa.
        Primero como un hobbie, regalando las primeras creaciones a sus nietos, entre ellos "Paco". 
        Paco para poder inscribirse en la facultad vendió la guitarra que le había hecho su abuelo.
        Y allí inspirado en la alegría de esa persona al recibir su primera guitarra y la oportunidad de hacer feliz a la gente y crear un gran negocio, Paco convenció a su abuelo de arrancar su primera tienda de música y la llamó "Instrumentos Musicales".
    </p>
    <p class="text-justify">
        La primera tienda fue en el viejo taller del abuelo Edmundo, que se ubicaba en la ciudad de Sauce, Corrientes. 
        Un local muy pequeño pero acogedor y siempre con una atención amable que invitaba a la gente a siempre volver. 
        Paco se encargaba de las finanzas y gerenciamiento del local, el abuelo Edmundo era el fabricante y la abuela Norma atendía al público.
    </p>
    <p class="text-justify">
        En el año 1994 fallece el abuelo Edmundo, principal fundador de esta gran empresa, lo recordamos siempre con afecto y contuinamos su legado
        a través de la música, sus enseñanzas, la amabilidad con nuestros clientes y la calidad en los instrumentos.
    </p>